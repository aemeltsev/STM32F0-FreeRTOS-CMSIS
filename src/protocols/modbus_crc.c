uint16_t modbus_crc16(const uint8_t *data, uint16_t length)
{
    // Стартовое значение регистра CRC по спецификации Modbus RTU всегда 0xFFFF
    uint16_t crc = 0xFFFF; 
    
    for (uint8_t i = 0; i < length; i++)
    {
        // 1. Вычисляем индекс в таблице с помощью операции XOR текущего байта и младшего байта CRC
        uint8_t table_index = (uint8_t)(crc ^ data[i]);
        
        // 2. Сдвигаем CRC на 8 бит вправо и подмешиваем значение из таблицы
        crc = (uint16_t)((crc >> 8) ^ crc16_table[table_index]);
    }
    
    return crc;
}
